/* jshint node:true */
'use strict';

var fs        = require('fs');
var path      = require('path');
var Sequelize = require('sequelize');
var env       = process.env.NODE_ENV || 'development';
var config    = require(__dirname + '/../config/config.json');
var data      = config.databases[env];
var sequelize = new Sequelize(data.database, data.username, data.password, data);
var db        = {};

fs
  .readdirSync(__dirname)
  // Read every file in models dir (except index.js)
  .filter(function(file) {
    return (file.indexOf('.') !== 0) && (file !== 'index.js');
  })
  .forEach(function(file) {
    // Import each module
    var model = sequelize.import(path.join(__dirname, file));
    // Add to db object
    db[model.name] = model;
  });

Object.keys(db).forEach(function(modelName) {
  // Check each model for an 'associate' method
  if ('associate' in db[modelName]) {
    // If found, run that method
    db[modelName].associate(db);
  }
});

db.sequelize = sequelize;
db.Sequelize = Sequelize;

module.exports = db;
